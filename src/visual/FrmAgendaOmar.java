    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) jtagenda.getModel();
        SimpleDateFormat formatodeltexto = new SimpleDateFormat("yyyyy-MM-dd");
        int fila = jtagenda.getSelectedRow();
        if (fila >= 0) {
            try{
                this.codigo=jtagenda.getValueAt(fila, 0).toString();
                this.cedula=jtagenda.getValueAt(fila, 1).toString();
                this.nombre =jtagenda.getValueAt(fila, 2).toString();
                String fecha = jtagenda.getValueAt(fila, 3).toString().trim();
                Date dato = null;
                try {
                    dato = (Date) formatodeltexto.parse(fecha);
                    this.f=dato;
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
                this.ob =jtagenda.getValueAt(fila, 5).toString();
                FrmRegistros frmreg = new FrmRegistros(this, true);
                frmreg.Rcedula=cedula;
                frmreg.Rcodigo=codigo;
                frmreg.Rnombre=nombre;
                frmreg.Rob=ob;
                frmreg.fe=f;
                frmreg.setVisible(true);
            }catch(NullPointerException d){
                JOptionPane.showMessageDialog(null, "No hay Registro", "Aviso", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "No Selecciono Ninguna Fila", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

