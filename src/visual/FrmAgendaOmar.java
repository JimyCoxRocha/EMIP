    private void jSpinner1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinner1StateChanged
        // TODO add your handling code here:

        valorSelecionado=Integer.parseInt(jSpinner1.getValue().toString());
        int x = jComboMeses.getSelectedIndex();
        int anio = Integer.parseInt(jComboAnio.getSelectedItem().toString());

        if (valorSelecionado == -1) {
            if (jComboMeses.getSelectedIndex() != 11) {
                jComboMeses.setSelectedIndex(x + 1);
            } else {
                jComboMeses.setSelectedIndex(0);
                anio = anio + 1;
                jComboAnio.setSelectedItem(anio);
            }
        }

        if(valorSelecionado==1){
            if (jComboMeses.getSelectedIndex() != 0) {
                jComboMeses.setSelectedIndex(x - 1);
            } else {
                jComboMeses.setSelectedIndex(11);
                anio = anio - 1;
                jComboAnio.setSelectedItem(anio);
            }
        }
        jSpinner1.setValue(0);
    }//GEN-LAST:event_jSpinner1StateChanged

