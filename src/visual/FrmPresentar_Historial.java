private void borrar(){
        DefaultTableModel modelo=(DefaultTableModel)tbPresentacion.getModel();
        if(modelo.getRowCount()!=0){
            while(modelo.getRowCount()>0)
            {
                modelo.removeRow(0);
            }
        } 
    }
    private int contar(){
        int cantidad=0;
        DefaultTableModel modelo=(DefaultTableModel)tbPresentacion.getModel();
        cantidad=modelo.getRowCount();
        return cantidad;
    }
